/**
 * learningSessions: One row per AI tutoring session a student starts.
 *
 * Session resume flow:
 *   1. Student opens a chapter.
 *   2. Query: WHERE studentId = ? AND chapterId = ? AND status IN ('ACTIVE','PAUSED').
 *   3. If found → read currentChunkIndex.
 *   4. Query pdfChunks WHERE chapterId = ? AND orderInChapter >= currentChunkIndex.
 *   5. Resume AI agent from that exact chunk — student continues seamlessly.
 *
 * currentChunkId:    FK to the exact pdfChunks row where session was paused.
 * currentChunkIndex: integer copy of orderInChapter (avoids a join on resume).
 * durationSeconds:   more granular than the old durationMinutes field.
 * chunksVisited:     ordered array of chunk IDs visited (for analytics).
 */
export const learningSessions = pgTable(
  "learning_sessions",
  {
    id: text("id")
      .primaryKey()
      .$defaultFn(() => nanoid()),
    studentId: text("student_id")
      .notNull()
      .references(() => userProfiles.id, { onDelete: "cascade" }),
    chapterId: text("chapter_id")
      .notNull()
      .references(() => chapters.id, { onDelete: "cascade" }),
    bookId: text("book_id")
      .notNull()
      .references(() => books.id, { onDelete: "cascade" }),
    agentId: text("agent_id").references(() => agents.id, {
      onDelete: "set null",
    }),
    title: text("title").notNull(),
    /** "full_explanation" | "quick_summary" | "weak_topics" */
    sessionType: text("session_type").notNull().default("full_explanation"),
    status: learningSessionStatus("status").notNull().default("UPCOMING"),
    /**
     * FK to the exact pdfChunks row where the student paused.
     * Null until the session transitions to ACTIVE.
     * Set to null again when status = COMPLETED.
     */
    currentChunkId: text("current_chunk_id").references(() => pdfChunks.id, {
      onDelete: "set null",
    }),
    /**
     * Integer copy of pdfChunks.orderInChapter for currentChunkId.
     * Stored denormalized so the resume query needs no join on pdfChunks.
     */
    currentChunkIndex: integer("current_chunk_index"),
    languagePreference: languagePreference("language_preference")
      .notNull()
      .default("ENGLISH"),
    startedAt: timestamp("started_at"),
    endedAt: timestamp("ended_at"),
    /** Actual session duration in seconds. */
    durationSeconds: integer("duration_seconds"),
    /** Full session transcript accumulated from OpenAI Realtime delta events. */
    transcript: text("transcript"),
    /** AI-generated study summary shown to student after session ends. */
    summary: text("summary"),
    /** AI-generated bullet-point notes for the student to review later. */
    aiNotes: text("ai_notes"),
    studentFeedback: text("student_feedback"),
    parentNotes: text("parent_notes"),
    /** Ordered array of pdfChunks IDs visited this session. */
    chunksVisited: jsonb("chunks_visited"),
    /** Ordered array of bookPages IDs viewed this session. */
    pagesVisited: jsonb("pages_visited"),
    createdAt: timestamp("created_at").notNull().defaultNow(),
    updatedAt: timestamp("updated_at").notNull().defaultNow(),
  },
  (t) => [
    /** THE resume query: student + chapter + active/paused status. */
    index("idx_learning_sessions_student_chapter").on(t.studentId, t.chapterId),
    index("idx_learning_sessions_student_status").on(t.studentId, t.status),
    index("idx_learning_sessions_chapter_status").on(t.chapterId, t.status),
    /** Parent dashboard: fetch all recent sessions for a child. */
    index("idx_learning_sessions_student_created").on(t.studentId, t.createdAt),
  ],
);

