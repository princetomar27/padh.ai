/**
 * pdfChunks: Paragraph/section-level text chunks extracted from each page.
 *
 * This is THE most critical table for the padh.ai tutoring feature.
 *
 * How the pipeline produces this:
 *   1. Inngest job fetches the original PDF from Supabase Storage.
 *   2. pdfjs-dist extracts text items with bounding-box coordinates
 *      (x, y, width, height in PDF user units = points, 1pt = 1/72 inch).
 *   3. Nearby text items are grouped into paragraph-level chunks.
 *   4. For chunks where pdfjs-dist returns garbled text (equations, images),
 *      GPT-4o Vision is called with the page image to generate a description.
 *   5. speakText is set to the final text the AI agent should speak.
 *   6. orderInChapter is a global sequential integer across all pages
 *      of the chapter, driving the AI session playback order.
 *
 * How the frontend uses this at runtime:
 *   1. AI agent speaks speakText for the current chunk.
 *   2. Frontend receives the chunkId via an OpenAI Realtime custom event.
 *   3. Frontend fetches boundingBoxes for that chunk.
 *   4. Frontend converts PDF-unit coordinates to pixel offsets:
 *        pixelX = (box.x / box.pageWidth)  * renderedImageWidthPx
 *        pixelY = (box.y / box.pageHeight) * renderedImageHeightPx
 *   5. A highlight <div> overlay is positioned on top of the page image,
 *      creating the live paragraph-highlight effect.
 *
 * boundingBoxes JSONB schema (array of objects):
 *   [{ x: number, y: number, width: number, height: number,
 *      pageWidth: number, pageHeight: number }]
 */
export const pdfChunks = pgTable(
  "pdf_chunks",
  {
    id: text("id")
      .primaryKey()
      .$defaultFn(() => nanoid()),
    bookPageId: text("book_page_id")
      .notNull()
      .references(() => bookPages.id, { onDelete: "cascade" }),
    chapterId: text("chapter_id")
      .notNull()
      .references(() => chapters.id, { onDelete: "cascade" }),
    /** Zero-based index of this chunk within its page. */
    chunkIndex: integer("chunk_index").notNull(),
    /**
     * Global ordering across ALL pages of the chapter (0-based).
     * Drives sequential AI playback: ORDER BY orderInChapter ASC.
     * Also used for resume: WHERE orderInChapter >= currentChunkIndex.
     */
    orderInChapter: integer("order_in_chapter").notNull(),
    /** Raw text as extracted by pdfjs-dist. May be garbled for equations. */
    text: text("text").notNull(),
    /**
     * Bounding box coordinates for every text run in this chunk.
     * Schema: { x, y, width, height, pageWidth, pageHeight }[]
     * All values are in PDF user units (points).
     * Frontend scales to pixel offsets using the rendered image dimensions.
     */
    boundingBoxes: jsonb("bounding_boxes").notNull(),
    /**
     * True when pdfjs-dist cannot reliably extract the text (math equations).
     * When true, equationDescription is used for TTS instead of text.
     */
    isEquation: boolean("is_equation").notNull().default(false),
    /** GPT-4o Vision spoken description of the equation. Null if not an equation. */
    equationDescription: text("equation_description"),
    /** True when this chunk is primarily an embedded image or diagram. */
    isImage: boolean("is_image").notNull().default(false),
    /** GPT-4o Vision spoken description of the image. Null if not an image. */
    imageDescription: text("image_description"),
    /**
     * The exact text the AI agent passes to OpenAI Realtime API TTS.
     * - Normal text  → cleaned version of the text field.
     * - Equation     → equationDescription in natural spoken form.
     * - Image        → imageDescription narrated naturally.
     * Always safe to pass directly; never contains raw LaTeX or garbled chars.
     */
    speakText: text("speak_text").notNull(),
    createdAt: timestamp("created_at").notNull().defaultNow(),
    updatedAt: timestamp("updated_at").notNull().defaultNow(),
  },
  (t) => [
    /** THE hottest query: fetch all chunks for a chapter in playback order. */
    index("idx_pdf_chunks_chapter_order").on(t.chapterId, t.orderInChapter),
    /** Prevent duplicate extraction on re-processing runs. */
    uniqueIndex("uq_pdf_chunks_page_chunk_idx").on(t.bookPageId, t.chunkIndex),
    index("idx_pdf_chunks_book_page_id").on(t.bookPageId),
    /** Let the pipeline handle equation chunks separately. */
    index("idx_pdf_chunks_chapter_equation").on(t.chapterId, t.isEquation),
  ],
);

